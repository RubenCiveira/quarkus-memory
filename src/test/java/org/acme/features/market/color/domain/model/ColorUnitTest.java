package org.acme.features.market.color.domain.model;

import java.util.Optional;

import org.acme.features.market.color.domain.model.valueobject.ColorMerchantVO;
import org.acme.features.market.color.domain.model.valueobject.ColorNameVO;
import org.acme.features.market.color.domain.model.valueobject.ColorUidVO;
import org.acme.features.market.color.domain.model.valueobject.ColorVersionVO;
import org.acme.features.market.merchant.domain.model.MerchantReference;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

class ColorUnitTest {

  /**
   * @autogenerated ValueObjectGenerator
   */
  @Test
  @DisplayName("Test Color contruction")
  void test_color_builder() {
    Color entity = Color.builder().uid(ColorUidVO.from("one")).name(ColorNameVO.from("one"))
        .merchant(ColorMerchantVO.from(MerchantReference.of("one"))).version(ColorVersionVO.from(1))
        .build();
    Assertions.assertEquals("one", entity.getUid().getValue());
    Assertions.assertEquals("one", entity.getUidValue());
    Assertions.assertEquals("one", entity.getName().getValue());
    Assertions.assertEquals("one", entity.getNameValue());
    Assertions.assertEquals(MerchantReference.of("one"),
        entity.getMerchant().getValue().orElse(null));
    Assertions.assertEquals(MerchantReference.of("one"), entity.getMerchantValue().orElse(null));
    Assertions.assertEquals(1, entity.getVersion().getValue().orElse(null));
    Assertions.assertEquals(1, entity.getVersionValue().orElse(null));
    Assertions.assertEquals("two", entity.withUidValue("two").getUidValue());
    Assertions.assertEquals("two", entity.withNameValue("two").getNameValue());
    Assertions.assertEquals(MerchantReference.of("two"),
        entity.withMerchantValue(Optional.of(MerchantReference.of("two"))).getMerchantValue()
            .orElse(null));
    Assertions
        .assertNull(entity.withMerchantValue(Optional.empty()).getMerchantValue().orElse(null));
    Assertions.assertNull(entity.withEmptyMerchant().getMerchantValue().orElse(null));
    Assertions.assertEquals(2,
        entity.withVersionValue(Optional.of(2)).getVersionValue().orElse(null));
    Assertions.assertNull(entity.withVersionValue(Optional.empty()).getVersionValue().orElse(null));
    Assertions.assertNull(entity.withEmptyVersion().getVersionValue().orElse(null));
    Assertions.assertEquals(entity, Color.builder().uidValue("one").nameValue("one")
        .merchantValue(MerchantReference.of("one")).versionValue(1).build());
  }
}
