package org.acme.features.market.color.application.usecase;

import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.color.application.allow.ColorDeleteAllow;
import org.acme.features.market.color.application.interaction.ColorDto;
import org.acme.features.market.color.application.interaction.command.ColorDeleteCommand;
import org.acme.features.market.color.application.interaction.query.ColorEntityAllowQuery;
import org.acme.features.market.color.application.interaction.result.ColorDeleteResult;
import org.acme.features.market.color.application.usecase.service.ColorsVisibilityService;
import org.acme.features.market.color.domain.Colors;
import org.acme.features.market.color.domain.gateway.ColorFilter;
import org.acme.features.market.color.domain.gateway.ColorWriteRepositoryGateway;
import org.acme.features.market.color.domain.model.Color;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class DeleteColorUsecase {

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Colors aggregate;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<ColorDeleteAllow> deleteAllow;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final ColorWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final ColorsVisibilityService visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public ColorDeleteAllow allow(final ColorEntityAllowQuery query) {
    ColorDeleteAllow base =
        ColorDeleteAllow.build(query.getReference(), true, "Allowed by default");
    deleteAllow.fire(base);
    return base;
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @return The slide with some values
   */
  public ColorDeleteResult delete(final ColorDeleteCommand command) {
    CompletionStage<Optional<Color>> updated = allow(command).getDetail().thenCompose(detail -> {
      if (!detail.isAllowed()) {
        throw new NotAllowedException(detail.getDescription());
      }
      ColorFilter filter = ColorFilter.builder().uid(command.getReference().getUidValue()).build();
      return visibility.visibleFilter(command, filter)
          .thenCompose(visibleFilter -> gateway
              .retrieve(command.getReference().getUidValue(), Optional.of(visibleFilter))
              .thenCompose(this::deleteIfIsPresent));
    });
    return ColorDeleteResult.builder().command(command)
        .color(updated.thenCompose(entity -> mapEntity(command, entity))).build();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  private ColorDeleteAllow allow(final ColorDeleteCommand query) {
    return allow(ColorEntityAllowQuery.builder().reference(query.getReference()).build(query));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param original
   * @return The slide with some values
   */
  private CompletionStage<Optional<Color>> deleteEntity(final Color original) {
    return aggregate.clean(original)
        .thenCompose(color -> gateway.delete(color).thenApply(Optional::of));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param result
   * @return The slide with some values
   */
  private CompletionStage<Optional<Color>> deleteIfIsPresent(final Optional<Color> result) {
    return result.map(this::deleteEntity)
        .orElseGet(() -> CompletableFuture.completedFuture(Optional.empty()));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @param opcolor
   * @return The slide with some values
   */
  private CompletionStage<Optional<ColorDto>> mapEntity(final ColorDeleteCommand command,
      final Optional<Color> opcolor) {
    return opcolor.map(color -> visibility.hide(command, color).thenApply(Optional::of))
        .orElseGet(() -> CompletableFuture.completedFuture(Optional.empty()));
  }
}
