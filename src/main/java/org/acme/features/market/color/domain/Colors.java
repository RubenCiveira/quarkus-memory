package org.acme.features.market.color.domain;

import java.util.Map;
import java.util.Map.Entry;
import java.util.Optional;
import java.util.Set;
import java.util.concurrent.CompletionStage;
import java.util.function.BiFunction;

import org.acme.common.action.ParametrizedFuturePipe;
import org.acme.common.action.PriorityComparator;
import org.acme.features.market.color.domain.model.Color;
import org.acme.features.market.color.domain.model.Color.ColorBuilder;
import org.acme.features.market.color.domain.rule.ColorActionType;
import org.acme.features.market.color.domain.rule.ColorBuilderRule;
import org.acme.features.market.color.domain.rule.ColorRule;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.inject.Instance;

@RequestScoped
public class Colors {

  /**
   * @autogenerated AggregateGenerator
   */
  private final ParametrizedFuturePipe<ColorBuilder, ColorActionType, Optional<Color>> builderRules;

  /**
   * @autogenerated AggregateGenerator
   */
  private final ParametrizedFuturePipe<Color, ColorActionType, Optional<Color>> rules;

  /**
   * @autogenerated AggregateGenerator
   */
  private Map<String, BiFunction<CompletionStage<ColorBuilder>, Optional<Color>, CompletionStage<ColorBuilder>>> calculatedFields =
      Map.of();

  /**
   * @autogenerated AggregateGenerator
   * @param rules
   * @param builderRules
   */
  public Colors(final Instance<ColorRule> rules, final Instance<ColorBuilderRule> builderRules) {
    this.rules = new ParametrizedFuturePipe<>(ColorActionType.values(), rules.stream().toList(),
        new PriorityComparator<>());
    this.builderRules = new ParametrizedFuturePipe<>(ColorActionType.values(),
        builderRules.stream().toList(), new PriorityComparator<>());
  }

  /**
   * @autogenerated AggregateGenerator
   * @return
   */
  public Set<String> calcultadFields() {
    return calculatedFields.keySet();
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param entity a filter to retrieve only matching values
   * @return The entity
   */
  public CompletionStage<Color> clean(final Color entity) {
    return rules.applyCurrent(ColorActionType.DELETE, entity, Optional.of(entity));
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param builder a filter to retrieve only matching values
   * @return The entity
   */
  public CompletionStage<Color> initialize(final ColorBuilder builder) {
    return applyModify(ColorActionType.CREATE, Optional.empty(), builder, null);
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param base a filter to retrieve only matching values
   * @param builder a filter to retrieve only matching values
   * @return The entity
   */
  public CompletionStage<Color> modify(final Color base, final ColorBuilder builder) {
    return applyModify(ColorActionType.UPDATE, Optional.of(base), builder, null);
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param type
   * @param original a filter to retrieve only matching values
   * @param builder a filter to retrieve only matching values
   * @param ignore
   * @return The entity
   */
  private CompletionStage<Color> applyModify(final ColorActionType type,
      final Optional<Color> original, final ColorBuilder builder, final String ignore) {
    CompletionStage<ColorBuilder> ruledBuilder =
        this.builderRules.applyCurrent(type, builder, original);
    for (Entry<String, BiFunction<CompletionStage<ColorBuilder>, Optional<Color>, CompletionStage<ColorBuilder>>> entry : calculatedFields
        .entrySet()) {
      if (!entry.getKey().equals(ignore)) {
        ruledBuilder = entry.getValue().apply(ruledBuilder, original);
      }
    }
    return ruledBuilder
        .thenCompose(resultBuilder -> rules.applyCurrent(type, resultBuilder.build(), original));
  }
}
