package org.acme.features.market.color.application.interaction.visibility;

import java.util.Optional;
import java.util.Set;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.features.market.color.domain.model.ColorRef;

import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NonNull;
import lombok.ToString;
import lombok.experimental.SuperBuilder;

@Getter
@ToString
@SuperBuilder(toBuilder = true)
@EqualsAndHashCode(callSuper = true, onlyExplicitlyIncluded = true)
public class ColorFixedFields extends Interaction {

  /**
   * @autogenerated FixedFieldsGenerator
   */
  private ColorRef color;

  /**
   * The list of fixed fields
   *
   * @autogenerated FixedFieldsGenerator
   */
  @NonNull
  private CompletionStage<Set<String>> fixed;

  /**
   * @autogenerated FixedFieldsGenerator
   * @return
   */
  public Optional<ColorRef> getColor() {
    return Optional.ofNullable(color);
  }
}
