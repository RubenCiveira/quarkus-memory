package org.acme.features.market.color.application.usecase.update;

import org.acme.common.action.Interaction;
import org.acme.features.market.color.application.ColorDto;
import org.acme.features.market.color.application.service.proposal.ColorModifyProposal;
import org.acme.features.market.color.domain.model.Color;

import jakarta.enterprise.event.Event;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.experimental.SuperBuilder;

@Data
@SuperBuilder(toBuilder = true)
@EqualsAndHashCode(callSuper = true, onlyExplicitlyIncluded = true)
public class ColorUpdateProposal extends ColorModifyProposal {

  /**
   * @autogenerated UpdateProposalGenerator
   * @param emitter
   * @param interaction
   * @param dto
   * @param entity
   * @return
   */
  public static ColorDto resolveWith(final Event<ColorUpdateProposal> emitter,
      final Interaction interaction, final ColorDto dto, final Color entity) {
    return resolveWith(emitter,
        ColorUpdateProposal.builder().interaction(interaction).dto(dto).entity(entity).build());
  }
}
