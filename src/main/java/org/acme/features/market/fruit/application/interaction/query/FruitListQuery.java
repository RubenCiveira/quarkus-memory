package org.acme.features.market.fruit.application.interaction.query;

import org.acme.common.action.Interaction;
import org.acme.features.market.fruit.domain.gateway.FruitCursor;
import org.acme.features.market.fruit.domain.gateway.FruitFilter;

import io.quarkus.runtime.annotations.RegisterForReflection;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.experimental.SuperBuilder;

@Data
@SuperBuilder(toBuilder = true)
@EqualsAndHashCode(callSuper = true, onlyExplicitlyIncluded = true)
@RegisterForReflection
public class FruitListQuery extends Interaction {

  /**
   * The cursor to retrieve a sub set of results
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private final FruitCursor cursor;

  /**
   * The filter to select the results to retrieve
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private final FruitFilter filter;
}
