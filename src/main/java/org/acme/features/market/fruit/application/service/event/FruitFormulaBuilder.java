package org.acme.features.market.fruit.application.service.event;

import java.util.Optional;
import java.util.concurrent.CompletionStage;
import java.util.function.Consumer;

import org.acme.common.action.Interaction;
import org.acme.features.market.fruit.application.FruitDto;
import org.acme.features.market.fruit.domain.model.Fruit;

import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.experimental.SuperBuilder;

@Data
@SuperBuilder(toBuilder = true)
@EqualsAndHashCode(callSuper = true, onlyExplicitlyIncluded = true)
public class FruitFormulaBuilder extends Interaction {

  /**
   * @autogenerated VisibleContentGenerator
   */
  Fruit original;

  /**
   * @autogenerated VisibleContentGenerator
   */
  @NonNull
  private CompletionStage<FruitDto> dto;

  /**
   * @autogenerated VisibleContentGenerator
   * @return
   */
  public Optional<Fruit> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated VisibleContentGenerator
   * @param mapper
   */
  public void tap(Consumer<FruitDto> mapper) {
    dto = dto.thenApply(dto -> {
      mapper.accept(dto);
      return dto;
    });
  }
}
