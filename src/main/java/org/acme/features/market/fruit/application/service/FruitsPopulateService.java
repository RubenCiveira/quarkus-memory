package org.acme.features.market.fruit.application.service;

import java.util.Map;
import java.util.Optional;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.validation.AbstractFailList;
import org.acme.common.validation.ConstraintFail;
import org.acme.features.market.fruit.application.FruitDto;
import org.acme.features.market.fruit.application.FruitPopulateResult;
import org.acme.features.market.fruit.domain.gateway.FruitCursor;
import org.acme.features.market.fruit.domain.gateway.FruitFilter;
import org.acme.features.market.fruit.domain.model.valueobject.FruitNameVO;

import jakarta.enterprise.context.RequestScoped;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class FruitsPopulateService {

  /**
   * @autogenerated PopulateServiceGenerator
   */
  private final FruitsVisibilityService fruitVisibles;

  /**
   * @autogenerated PopulateServiceGenerator
   * @param interaction
   * @param map
   * @param fails
   * @return
   */
  public CompletionStage<Optional<FruitPopulateResult>> populateInto(Interaction interaction,
      Map<String, String> map, AbstractFailList fails) {
    AbstractFailList constraints = new AbstractFailList();
    FruitFilter.FruitFilterBuilder filterBuilder = FruitFilter.builder();
    Optional<String> hasName = Optional.ofNullable(map.get("name"));
    if (hasName.isPresent()) {
      filterBuilder = filterBuilder.name(hasName.get());
    } else {
      constraints.add(new ConstraintFail("name-required", "The name is required", null));
    }
    return fruitVisibles.listCachedVisibles(interaction, filterBuilder.build(),
        FruitCursor.builder().limit(1).build()).thenApply(results -> {
          FruitDto.FruitDtoBuilder builder = FruitDto.builder();
          Optional.ofNullable(map.get("Name")).map(value -> FruitNameVO.tryFrom(value, constraints))
              .ifPresent(builder::name);
          if (constraints.isEmpty()) {
            return Optional.of(FruitPopulateResult.builder().original(results.first().orElse(null))
                .dto(builder.build()).build());
          } else {
            fails.add(constraints);
            return Optional.empty();
          }
        });
  }
}
