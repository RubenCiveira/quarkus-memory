package org.acme.features.market.fruit.application.usecase.delete;

import java.util.List;

import org.acme.common.batch.stepper.ItemDescriptor;
import org.acme.common.batch.stepper.ItemProcessor;
import org.acme.common.batch.stepper.ItemReader;
import org.acme.common.batch.stepper.ItemWriter;
import org.acme.common.batch.stepper.StepContext;
import org.acme.common.batch.stepper.StepCounter;
import org.acme.common.batch.stepper.StepFinalizer;
import org.acme.common.batch.stepper.StepInitializer;
import org.acme.common.exception.ExecutionException;
import org.acme.common.security.Allow;
import org.acme.features.market.fruit.application.service.FruitsVisibilityService;
import org.acme.features.market.fruit.domain.gateway.FruitCursor;
import org.acme.features.market.fruit.domain.model.Fruit;

import io.quarkus.arc.Unremovable;
import jakarta.enterprise.context.RequestScoped;
import lombok.Data;
import lombok.RequiredArgsConstructor;

@Unremovable
@RequestScoped
@RequiredArgsConstructor
class DeleteFruitsInBatchExecutor implements
    StepCounter<FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    StepFinalizer<FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    StepInitializer<FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    ItemReader<Fruit, FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    ItemWriter<Fruit, FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    ItemProcessor<Fruit, Fruit, FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch>,
    ItemDescriptor<Fruit, FruitDeleteAllInBatchCommand, DeleteFruitsInBatchExecutor.FruitPaginableBatch> {

  /**
   * @autogenerated EntityGenerator
   */
  @Data
  public static class FruitPaginableBatch {

    /**
     * @autogenerated EntityGenerator
     */
    private String since;
  }

  /**
   * @autogenerated EntityGenerator
   */
  private final int size = 10;

  /**
   * @autogenerated EntityGenerator
   */
  private final DeleteFruitUsecase usecase;

  /**
   * @autogenerated EntityGenerator
   */
  private final FruitsVisibilityService visibility;

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public long approximatedItems(
      final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    return visibility.countVisibles(context.getParams(), context.getParams().getFilter());
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void finish(final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    usecase.flush();
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   */
  @Override
  public void init(final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    context.setState(new FruitPaginableBatch());
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public String itemDescription(final Fruit item,
      final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    return item.getUidValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @param item
   * @param context
   * @return
   */
  @Override
  public Fruit process(final Fruit item,
      final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    Allow allowed = usecase.allow(context.getParams(), item);
    if (!allowed.isAllowed()) {
      throw new ExecutionException("not-allowed", null);
    }
    return item;
  }

  /**
   * @autogenerated EntityGenerator
   * @param context
   * @return
   */
  @Override
  public List<Fruit> read(
      final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    List<Fruit> page = visibility.listVisibles(context.getParams(), context.getParams().getFilter(),
        FruitCursor.builder().limit(size).sinceUid(context.getState().getSince()).build());
    context.getState().setSince(page.isEmpty() ? null : page.get(page.size() - 1).getUidValue());
    return page;
  }

  /**
   * @autogenerated EntityGenerator
   * @param items
   * @param context
   */
  @Override
  public void write(final List<Fruit> items,
      final StepContext<FruitDeleteAllInBatchCommand, FruitPaginableBatch> context) {
    items.forEach(item -> usecase.delete(context.getParams(), item));
  }
}
