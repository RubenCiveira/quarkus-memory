package org.acme.features.market.fruit.application.service.event;

import java.util.function.Consumer;
import java.util.function.Function;
import java.util.function.UnaryOperator;
import org.acme.common.action.Interaction;
import org.acme.common.reactive.Stream;
import org.acme.features.market.fruit.domain.gateway.FruitFilter;
import lombok.Builder;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.With;

@Data
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class FruitVisibilityQueryPipelineStageEvent {

  /**
   * @autogenerated VisibleContentPipelineStageEventGenerator
   */
  @NonNull
  private final Interaction interaction;

  /**
   * @autogenerated VisibleContentPipelineStageEventGenerator
   */
  @NonNull
  private Stream<FruitFilter> filter;

  /**
   * @autogenerated VisibleContentPipelineStageEventGenerator
   * @param mapper
   */
  public void flatMap(Function<FruitFilter, Stream<FruitFilter>> mapper) {
    filter = filter.flatMap(mapper);
  }

  /**
   * @autogenerated VisibleContentPipelineStageEventGenerator
   * @param mapper
   */
  public void map(UnaryOperator<FruitFilter> mapper) {
    filter = filter.map(mapper);
  }

  /**
   * @autogenerated VisibleContentPipelineStageEventGenerator
   * @param mapper
   */
  public void peek(Consumer<FruitFilter> mapper) {
    filter = filter.peek(filter -> {
      mapper.accept(filter);
    });
  }
}
