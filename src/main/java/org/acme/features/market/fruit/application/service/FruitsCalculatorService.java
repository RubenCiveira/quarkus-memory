package org.acme.features.market.fruit.application.service;

import org.acme.common.action.Interaction;
import org.acme.features.market.fruit.application.FruitDto;
import org.acme.features.market.fruit.application.service.proposal.FruitCalculatedProposal;
import org.acme.features.market.fruit.domain.model.Fruit;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class FruitsCalculatorService {

  /**
   * Event source for maniputale the visible list
   *
   * @autogenerated CalculatorServiceGenerator
   */
  private final Event<FruitCalculatedProposal> formulaBuilder;

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public FruitDto copyWithFormulas(Interaction prev, FruitDto source, Fruit original) {
    FruitCalculatedProposal formulas =
        FruitCalculatedProposal.builder().dto(source).original(original).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public FruitDto copyWithFormulas(Interaction prev, FruitDto source) {
    FruitCalculatedProposal formulas =
        FruitCalculatedProposal.builder().dto(source).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }
}
