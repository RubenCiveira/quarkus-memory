package org.acme.features.market.area.application.service;

import java.util.Map;
import java.util.Optional;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.validation.AbstractFailList;
import org.acme.common.validation.ConstraintFail;
import org.acme.features.market.area.application.AreaDto;
import org.acme.features.market.area.application.AreaPopulateResult;
import org.acme.features.market.area.domain.gateway.AreaCursor;
import org.acme.features.market.area.domain.gateway.AreaFilter;
import org.acme.features.market.area.domain.model.valueobject.AreaNameVO;
import org.acme.features.market.place.domain.model.PlaceRef;

import jakarta.enterprise.context.RequestScoped;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class AreasPopulateService {

  /**
   * @autogenerated PopulateServiceGenerator
   */
  private final AreasVisibilityService areaVisibles;

  /**
   * @autogenerated PopulateServiceGenerator
   * @param interaction
   * @param parent
   * @param map
   * @param fails
   * @return
   */
  public CompletionStage<Optional<AreaPopulateResult>> populateInto(Interaction interaction,
      PlaceRef parent, Map<String, String> map, AbstractFailList fails) {
    AbstractFailList constraints = new AbstractFailList();
    AreaFilter.AreaFilterBuilder filterBuilder = AreaFilter.builder();
    filterBuilder = filterBuilder.place(parent);
    Optional<String> hasName = Optional.ofNullable(map.get("name"));
    if (hasName.isPresent()) {
      filterBuilder = filterBuilder.name(hasName.get());
    } else {
      constraints.add(new ConstraintFail("name-required", "The name is required", null));
    }
    return areaVisibles.listCachedVisibles(interaction, filterBuilder.build(),
        AreaCursor.builder().limit(1).build()).thenApply(results -> {
          AreaDto.AreaDtoBuilder builder = AreaDto.builder();
          Optional.ofNullable(map.get("Name")).map(value -> AreaNameVO.tryFrom(value, constraints))
              .ifPresent(builder::name);
          if (constraints.isEmpty()) {
            return Optional.of(AreaPopulateResult.builder().original(results.first().orElse(null))
                .dto(builder.build()).build());
          } else {
            fails.add(constraints);
            return Optional.empty();
          }
        });
  }
}
