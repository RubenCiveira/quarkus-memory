package org.acme.features.market.area.application.usecase.list;

import java.util.List;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.area.application.AreaDto;
import org.acme.features.market.area.application.service.AreasVisibilityService;
import org.acme.features.market.area.application.usecase.list.event.AreaListAllowPipelineStageEvent;
import org.acme.features.market.area.domain.gateway.AreaCached;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class ListAreaUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<AreaListAllowPipelineStageEvent> listAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final AreasVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("area-list-allow").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      AreaListAllowPipelineStageEvent base =
          AreaListAllowPipelineStageEvent.build(query, true, "Allowed by default");
      listAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<AreaListResult> list(final AreaListQuery query) {
    Span startSpan = tracer.spanBuilder("area-list").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<AreaCached> future = allow(query).thenCompose(detail -> {
        if (!detail.isAllowed()) {
          throw new NotAllowedException(detail.getDescription());
        }
        return visibility.listCachedVisibles(query, query.getFilter(), query.getCursor());
      });
      return future.thenCompose(values -> mapList(query, values)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param areas
   * @return The slide with some values
   */
  private CompletionStage<AreaListResult> mapList(final AreaListQuery query,
      final AreaCached areas) {
    Span startSpan = tracer.spanBuilder("area-map-list-response").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      List<CompletableFuture<AreaDto>> futures =
          areas.getValue().stream().map(area -> visibility.copyWithHidden(query, area))
              .map(CompletionStage::toCompletableFuture).toList();
      return CompletableFuture.allOf(futures.toArray(new CompletableFuture[0]))
          .thenApply(voidResult -> AreaListResult.builder().query(query)
              .areas(futures.stream().map(CompletableFuture::join).toList()).since(areas.getSince())
              .build())
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
