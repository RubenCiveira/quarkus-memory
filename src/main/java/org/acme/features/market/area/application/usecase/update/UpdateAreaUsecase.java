package org.acme.features.market.area.application.usecase.update;

import java.util.Optional;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.exception.NotFoundException;
import org.acme.common.security.Allow;
import org.acme.features.market.area.application.AreaDto;
import org.acme.features.market.area.application.service.AreasVisibilityService;
import org.acme.features.market.area.domain.Areas;
import org.acme.features.market.area.domain.gateway.AreaCacheGateway;
import org.acme.features.market.area.domain.gateway.AreaWriteRepositoryGateway;
import org.acme.features.market.area.domain.model.Area;
import org.acme.features.market.area.domain.model.AreaRef;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class UpdateAreaUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Areas aggregate;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final AreaCacheGateway cache;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<AreaUpdateEvent> eventEmitter;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final AreaWriteRepositoryGateway gateway;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<AreaUpdateProposal> proposalEmitter;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<AreaUpdateAllowProposal> updateAllow;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final AreasVisibilityService visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public Allow allow(final Interaction query, final AreaRef reference) {
    return AreaUpdateAllowProposal.resolveWith(updateAllow,
        AreaUpdateAllowProposal.build(query, Optional.of(reference), true, "Allowed by default"));
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public Allow allow(final Interaction query) {
    return AreaUpdateAllowProposal.resolveWith(updateAllow,
        AreaUpdateAllowProposal.build(query, Optional.empty(), true, "Allowed by default"));
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @return The slide with some values
   */
  public AreaDto update(final AreaUpdateCommand command) {
    Allow detail = allow(command, command.getReference());
    if (!detail.isAllowed()) {
      throw new NotAllowedException(detail.getDescription());
    }
    Area original = visibility.retrieveVisible(command, command.getReference().getUidValue())
        .orElseThrow(() -> new NotFoundException(""));
    AreaDto filled = visibility.copyWithFixed(command, command.getDto());
    AreaDto dto = AreaUpdateProposal.resolveWith(proposalEmitter, command, filled, original);
    Area saved = gateway.update(original,
        aggregate.modify(original, dto.toEntityBuilder(Optional.of(original))));
    cache.update(saved);
    AreaUpdateEvent.notifyWith(eventEmitter, command, saved);
    return visibility.copyWithHidden(command, saved);
  }
}
