package org.acme.features.market.place.application.usecase.retrieve;

import java.time.OffsetDateTime;
import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.place.application.service.PlacesVisibilityService;
import org.acme.features.market.place.application.usecase.retrieve.event.PlaceRetrieveAllowPipelineStageEvent;
import org.acme.features.market.place.domain.gateway.PlaceCached;
import org.acme.features.market.place.domain.model.PlaceRef;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class RetrievePlaceUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<PlaceRetrieveAllowPipelineStageEvent> retrieveAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final PlacesVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query, final PlaceRef reference) {
    Span startSpan = tracer.spanBuilder("place-specific-allow-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      PlaceRetrieveAllowPipelineStageEvent base = PlaceRetrieveAllowPipelineStageEvent.build(query,
          Optional.of(reference), true, "Allowed by default");
      retrieveAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("place-generic-allow-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      PlaceRetrieveAllowPipelineStageEvent base = PlaceRetrieveAllowPipelineStageEvent.build(query,
          Optional.empty(), true, "Allowed by default");
      retrieveAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<PlaceRetrieveResult> retrieve(final PlaceRetrieveQuery query) {
    Span startSpan = tracer.spanBuilder("place-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<PlaceCached> result =
          allow(query, query.getReference()).thenCompose(detail -> {
            if (!detail.isAllowed()) {
              throw new NotAllowedException(detail.getDescription());
            }
            return visibility.retrieveCachedVisible(query, query.getReference().getUidValue());
          });
      return result.thenCompose(op -> this.mapEntity(query, op)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param opplace
   * @return The slide with some values
   */
  private CompletionStage<PlaceRetrieveResult> mapEntity(final PlaceRetrieveQuery query,
      final PlaceCached opplace) {
    Span startSpan = tracer.spanBuilder("place-map-retrieve-result").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      return opplace.first()
          .map(place -> visibility.copyWithHidden(query, place)
              .thenApply(placeWithHidden -> PlaceRetrieveResult.builder().interaction(query)
                  .place(Optional.of(placeWithHidden)).since(opplace.getSince()).build()))
          .orElseGet(() -> CompletableFuture.completedFuture(PlaceRetrieveResult.builder()
              .interaction(query).place(Optional.empty()).since(OffsetDateTime.now()).build()))
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
