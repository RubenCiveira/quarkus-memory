package org.acme.features.market.place.application.usecase.list;

import java.util.List;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.place.application.PlaceDto;
import org.acme.features.market.place.application.service.PlacesVisibilityService;
import org.acme.features.market.place.application.usecase.list.event.PlaceListAllowPipelineStageEvent;
import org.acme.features.market.place.domain.gateway.PlaceCached;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class ListPlaceUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<PlaceListAllowPipelineStageEvent> listAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final PlacesVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("place-list-allow").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      PlaceListAllowPipelineStageEvent base =
          PlaceListAllowPipelineStageEvent.build(query, true, "Allowed by default");
      listAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<PlaceListResult> list(final PlaceListQuery query) {
    Span startSpan = tracer.spanBuilder("place-list").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<PlaceCached> future = allow(query).thenCompose(detail -> {
        if (!detail.isAllowed()) {
          throw new NotAllowedException(detail.getDescription());
        }
        return visibility.listCachedVisibles(query, query.getFilter(), query.getCursor());
      });
      return future.thenCompose(values -> mapList(query, values)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param places
   * @return The slide with some values
   */
  private CompletionStage<PlaceListResult> mapList(final PlaceListQuery query,
      final PlaceCached places) {
    Span startSpan = tracer.spanBuilder("place-map-list-response").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      List<CompletableFuture<PlaceDto>> futures =
          places.getValue().stream().map(place -> visibility.copyWithHidden(query, place))
              .map(CompletionStage::toCompletableFuture).toList();
      return CompletableFuture.allOf(futures.toArray(new CompletableFuture[0]))
          .thenApply(voidResult -> PlaceListResult.builder().query(query)
              .places(futures.stream().map(CompletableFuture::join).toList())
              .since(places.getSince()).build())
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
