package org.acme.features.market.place.domain.model;

import java.time.OffsetDateTime;
import java.util.Optional;

import org.acme.features.market.merchant.domain.model.MerchantRef;
import org.acme.features.market.place.domain.model.valueobject.PlaceMerchantVO;
import org.acme.features.market.place.domain.model.valueobject.PlaceNameVO;
import org.acme.features.market.place.domain.model.valueobject.PlaceOpeningDateVO;
import org.acme.features.market.place.domain.model.valueobject.PlacePhotoVO;
import org.acme.features.market.place.domain.model.valueobject.PlaceUidVO;
import org.acme.features.market.place.domain.model.valueobject.PlaceVersionVO;

import lombok.Builder;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NonNull;
import lombok.ToString;
import lombok.With;

@Getter
@ToString
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class Place implements PlaceRef {

  /**
   * @autogenerated EntityGenerator
   */
  public static class PlaceBuilder {

    /**
     * @autogenerated EntityGenerator
     * @param merchant
     * @return
     */
    public PlaceBuilder merchantReferenceValue(final String merchant) {
      return merchant(PlaceMerchantVO.fromReference(merchant));
    }

    /**
     * @autogenerated EntityGenerator
     * @param merchant
     * @return
     */
    public PlaceBuilder merchantValue(final MerchantRef merchant) {
      return merchant(PlaceMerchantVO.from(merchant));
    }

    /**
     * @autogenerated EntityGenerator
     * @param name
     * @return
     */
    public PlaceBuilder nameValue(final String name) {
      return name(PlaceNameVO.from(name));
    }

    /**
     * @autogenerated EntityGenerator
     * @param openingDate
     * @return
     */
    public PlaceBuilder openingDateValue(final OffsetDateTime openingDate) {
      return openingDate(PlaceOpeningDateVO.from(openingDate));
    }

    /**
     * @autogenerated EntityGenerator
     * @param photo
     * @return
     */
    public PlaceBuilder photoValue(final String photo) {
      return photo(PlacePhotoVO.from(photo));
    }

    /**
     * @autogenerated EntityGenerator
     * @param uid
     * @return
     */
    public PlaceBuilder uidValue(final String uid) {
      return uid(PlaceUidVO.from(uid));
    }

    /**
     * @autogenerated EntityGenerator
     * @param version
     * @return
     */
    public PlaceBuilder versionValue(final Integer version) {
      return version(PlaceVersionVO.from(version));
    }
  }

  /**
   * El merchant de place
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private PlaceMerchantVO merchant;

  /**
   * El name de place
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private PlaceNameVO name;

  /**
   * El opening date de place
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  @Builder.Default
  private PlaceOpeningDateVO openingDate = PlaceOpeningDateVO.empty();

  /**
   * El photo de place
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  @Builder.Default
  private PlacePhotoVO photo = PlacePhotoVO.empty();

  /**
   * A number to identify the db record
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private PlaceUidVO uid;

  /**
   * Campo con el n√∫mero de version de place para controlar bloqueos optimistas
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  @Builder.Default
  private PlaceVersionVO version = PlaceVersionVO.empty();

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public String getMerchantReferenceValue() {
    return getMerchant().getReferenceValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public MerchantRef getMerchantValue() {
    return getMerchant().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public String getNameValue() {
    return getName().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Optional<OffsetDateTime> getOpeningDateValue() {
    return getOpeningDate().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Optional<String> getPhotoValue() {
    return getPhoto().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public String getUidValue() {
    return getUid().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Optional<Integer> getVersionValue() {
    return getVersion().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Place withEmptyOpeningDate() {
    return withOpeningDate(PlaceOpeningDateVO.empty());
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Place withEmptyPhoto() {
    return withPhoto(PlacePhotoVO.empty());
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Place withEmptyVersion() {
    return withVersion(PlaceVersionVO.empty());
  }

  /**
   * @autogenerated EntityGenerator
   * @param merchant
   * @return
   */
  public Place withMerchantReferenceValue(final String merchant) {
    return withMerchant(PlaceMerchantVO.fromReference(merchant));
  }

  /**
   * @autogenerated EntityGenerator
   * @param merchant
   * @return
   */
  public Place withMerchantValue(final MerchantRef merchant) {
    return withMerchant(PlaceMerchantVO.from(merchant));
  }

  /**
   * @autogenerated EntityGenerator
   * @param name
   * @return
   */
  public Place withNameValue(final String name) {
    return withName(PlaceNameVO.from(name));
  }

  /**
   * @autogenerated EntityGenerator
   * @param openingDate
   * @return
   */
  public Place withOpeningDateValue(final OffsetDateTime openingDate) {
    return withOpeningDate(PlaceOpeningDateVO.from(openingDate));
  }

  /**
   * @autogenerated EntityGenerator
   * @param openingDate
   * @return
   */
  public Place withOpeningDateValue(final Optional<OffsetDateTime> openingDate) {
    return openingDate.isPresent() ? withOpeningDate(PlaceOpeningDateVO.from(openingDate.get()))
        : withEmptyOpeningDate();
  }

  /**
   * @autogenerated EntityGenerator
   * @param photo
   * @return
   */
  public Place withPhotoValue(final String photo) {
    return withPhoto(PlacePhotoVO.from(photo));
  }

  /**
   * @autogenerated EntityGenerator
   * @param photo
   * @return
   */
  public Place withPhotoValue(final Optional<String> photo) {
    return photo.isPresent() ? withPhoto(PlacePhotoVO.from(photo.get())) : withEmptyPhoto();
  }

  /**
   * @autogenerated EntityGenerator
   * @param uid
   * @return
   */
  public Place withUidValue(final String uid) {
    return withUid(PlaceUidVO.from(uid));
  }

  /**
   * @autogenerated EntityGenerator
   * @param version
   * @return
   */
  public Place withVersionValue(final Integer version) {
    return withVersion(PlaceVersionVO.from(version));
  }

  /**
   * @autogenerated EntityGenerator
   * @param version
   * @return
   */
  public Place withVersionValue(final Optional<Integer> version) {
    return version.isPresent() ? withVersion(PlaceVersionVO.from(version.get()))
        : withEmptyVersion();
  }
}
