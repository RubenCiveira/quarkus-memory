package org.acme.features.market.place.application.service;

import java.util.Map;
import java.util.Optional;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.validation.AbstractFailList;
import org.acme.common.validation.ConstraintFail;
import org.acme.features.market.merchant.domain.model.MerchantRef;
import org.acme.features.market.place.application.PlaceDto;
import org.acme.features.market.place.application.PlacePopulateResult;
import org.acme.features.market.place.domain.gateway.PlaceCursor;
import org.acme.features.market.place.domain.gateway.PlaceFilter;
import org.acme.features.market.place.domain.model.valueobject.PlaceNameVO;
import org.acme.features.market.place.domain.model.valueobject.PlaceOpeningDateVO;

import jakarta.enterprise.context.RequestScoped;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class PlacesPopulateService {

  /**
   * @autogenerated PopulateServiceGenerator
   */
  private final PlacesVisibilityService placeVisibles;

  /**
   * @autogenerated PopulateServiceGenerator
   * @param interaction
   * @param parent
   * @param map
   * @param fails
   * @return
   */
  public CompletionStage<Optional<PlacePopulateResult>> populateInto(Interaction interaction,
      MerchantRef parent, Map<String, String> map, AbstractFailList fails) {
    AbstractFailList constraints = new AbstractFailList();
    PlaceFilter.PlaceFilterBuilder filterBuilder = PlaceFilter.builder();
    filterBuilder = filterBuilder.merchant(parent);
    Optional<String> hasName = Optional.ofNullable(map.get("name"));
    if (hasName.isPresent()) {
      filterBuilder = filterBuilder.name(hasName.get());
    } else {
      constraints.add(new ConstraintFail("name-required", "The name is required", null));
    }
    return placeVisibles.listCachedVisibles(interaction, filterBuilder.build(),
        PlaceCursor.builder().limit(1).build()).thenApply(results -> {
          PlaceDto.PlaceDtoBuilder builder = PlaceDto.builder();
          Optional.ofNullable(map.get("Name")).map(value -> PlaceNameVO.tryFrom(value, constraints))
              .ifPresent(builder::name);
          Optional.ofNullable(map.get("OpeningDate"))
              .map(value -> PlaceOpeningDateVO.tryFrom(value, constraints))
              .ifPresent(builder::openingDate);
          if (constraints.isEmpty()) {
            return Optional.of(PlacePopulateResult.builder().original(results.first().orElse(null))
                .dto(builder.build()).build());
          } else {
            fails.add(constraints);
            return Optional.empty();
          }
        });
  }
}
