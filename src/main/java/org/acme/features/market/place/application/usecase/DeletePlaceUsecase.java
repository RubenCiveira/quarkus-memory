package org.acme.features.market.place.application.usecase;

import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.place.application.allow.PlaceDeleteAllow;
import org.acme.features.market.place.application.interaction.PlaceDto;
import org.acme.features.market.place.application.interaction.command.PlaceDeleteCommand;
import org.acme.features.market.place.application.interaction.query.PlaceEntityAllowQuery;
import org.acme.features.market.place.application.interaction.result.PlaceDeleteResult;
import org.acme.features.market.place.application.usecase.service.PlacesVisibilityService;
import org.acme.features.market.place.domain.Places;
import org.acme.features.market.place.domain.gateway.PlaceFilter;
import org.acme.features.market.place.domain.gateway.PlaceWriteRepositoryGateway;
import org.acme.features.market.place.domain.model.Place;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class DeletePlaceUsecase {

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Places aggregate;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated DeleteUsecaseGenerator
   */
  private final Event<PlaceDeleteAllow> deleteAllow;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final PlaceWriteRepositoryGateway gateway;

  /**
   * @autogenerated DeleteUsecaseGenerator
   */
  private final PlacesVisibilityService visibility;

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  public PlaceDeleteAllow allow(final PlaceEntityAllowQuery query) {
    PlaceDeleteAllow base =
        PlaceDeleteAllow.build(query.getReference(), true, "Allowed by default");
    deleteAllow.fire(base);
    return base;
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @return The slide with some values
   */
  public PlaceDeleteResult delete(final PlaceDeleteCommand command) {
    CompletionStage<Optional<Place>> updated = allow(command).getDetail().thenCompose(detail -> {
      if (!detail.isAllowed()) {
        throw new NotAllowedException(detail.getDescription());
      }
      PlaceFilter filter = PlaceFilter.builder().uid(command.getReference().getUidValue()).build();
      return visibility.visibleFilter(command, filter)
          .thenCompose(visibleFilter -> gateway
              .retrieve(command.getReference().getUidValue(), Optional.of(visibleFilter))
              .thenCompose(this::deleteIfIsPresent));
    });
    return PlaceDeleteResult.builder().command(command)
        .place(updated.thenCompose(entity -> mapEntity(command, entity))).build();
  }

  /**
   * @autogenerated DeleteUsecaseGenerator
   * @param query
   * @return
   */
  private PlaceDeleteAllow allow(final PlaceDeleteCommand query) {
    return allow(PlaceEntityAllowQuery.builder().reference(query.getReference()).build(query));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param original
   * @return The slide with some values
   */
  private CompletionStage<Optional<Place>> deleteEntity(final Place original) {
    return aggregate.clean(original)
        .thenCompose(place -> gateway.delete(place).thenApply(Optional::of));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param result
   * @return The slide with some values
   */
  private CompletionStage<Optional<Place>> deleteIfIsPresent(final Optional<Place> result) {
    return result.map(this::deleteEntity)
        .orElseGet(() -> CompletableFuture.completedFuture(Optional.empty()));
  }

  /**
   * The slide with some values
   *
   * @autogenerated DeleteUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @param opplace
   * @return The slide with some values
   */
  private CompletionStage<Optional<PlaceDto>> mapEntity(final PlaceDeleteCommand command,
      final Optional<Place> opplace) {
    return opplace.map(place -> visibility.hide(command, place).thenApply(Optional::of))
        .orElseGet(() -> CompletableFuture.completedFuture(Optional.empty()));
  }
}
