package org.acme.features.market.verify.domain.model;

import java.util.Optional;

import org.acme.common.exception.ConstraintException;
import org.acme.common.validation.ConstraintFail;
import org.acme.common.validation.ConstraintFailList;
import org.acme.features.market.verify.domain.model.valueobject.VerifyNameVO;
import org.acme.features.market.verify.domain.model.valueobject.VerifyUidVO;
import org.acme.features.market.verify.domain.model.valueobject.VerifyVersionVO;

import lombok.Builder;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NonNull;
import lombok.ToString;
import lombok.With;

@Getter
@ToString
@Builder(toBuilder = true)
@With
@EqualsAndHashCode(onlyExplicitlyIncluded = true)
public class Verify implements VerifyRef {

  /**
   * @autogenerated EntityGenerator
   */
  public static class VerifyBuilder {

    /**
     * @autogenerated EntityGenerator
     * @return
     */
    public Verify buildValid() {
      ConstraintFailList list = new ConstraintFailList();
      if (null == uid) {
        list.add(new ConstraintFail("REQUIRED", "uid", null));
      }
      if (null == name) {
        list.add(new ConstraintFail("REQUIRED", "name", null));
      }
      if (list.hasErrors()) {
        throw new ConstraintException(list);
      }
      return build();
    }

    /**
     * @autogenerated EntityGenerator
     * @param name
     * @return
     */
    public VerifyBuilder nameValue(final String name) {
      return name(VerifyNameVO.from(name));
    }

    /**
     * @autogenerated EntityGenerator
     * @param uid
     * @return
     */
    public VerifyBuilder uidValue(final String uid) {
      return uid(VerifyUidVO.from(uid));
    }

    /**
     * @autogenerated EntityGenerator
     * @param version
     * @return
     */
    public VerifyBuilder versionValue(final Integer version) {
      return version(VerifyVersionVO.from(version));
    }
  }

  /**
   * El name de verify
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private VerifyNameVO name;

  /**
   * A number to identify the db record
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  private VerifyUidVO uid;

  /**
   * Campo con el n√∫mero de version de verify para controlar bloqueos optimistas
   *
   * @autogenerated EntityGenerator
   */
  @NonNull
  @Builder.Default
  private VerifyVersionVO version = VerifyVersionVO.empty();

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public String getNameValue() {
    return getName().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public String getUidValue() {
    return getUid().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Optional<Integer> getVersionValue() {
    return getVersion().getValue();
  }

  /**
   * @autogenerated EntityGenerator
   * @return
   */
  public Verify withEmptyVersion() {
    return withVersion(VerifyVersionVO.empty());
  }

  /**
   * @autogenerated EntityGenerator
   * @param name
   * @return
   */
  public Verify withNameValue(final String name) {
    return withName(VerifyNameVO.from(name));
  }

  /**
   * @autogenerated EntityGenerator
   * @param uid
   * @return
   */
  public Verify withUidValue(final String uid) {
    return withUid(VerifyUidVO.from(uid));
  }

  /**
   * @autogenerated EntityGenerator
   * @param version
   * @return
   */
  public Verify withVersionValue(final Integer version) {
    return withVersion(VerifyVersionVO.from(version));
  }

  /**
   * @autogenerated EntityGenerator
   * @param version
   * @return
   */
  public Verify withVersionValue(final Optional<Integer> version) {
    return version.isPresent() ? withVersion(VerifyVersionVO.from(version.get()))
        : withEmptyVersion();
  }
}
