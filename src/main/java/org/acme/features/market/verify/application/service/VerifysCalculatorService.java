package org.acme.features.market.verify.application.service;

import org.acme.common.action.Interaction;
import org.acme.features.market.verify.application.VerifyDto;
import org.acme.features.market.verify.application.service.proposal.VerifyCalculatedProposal;
import org.acme.features.market.verify.domain.model.Verify;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class VerifysCalculatorService {

  /**
   * Event source for maniputale the visible list
   *
   * @autogenerated CalculatorServiceGenerator
   */
  private final Event<VerifyCalculatedProposal> formulaBuilder;

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public VerifyDto copyWithFormulas(Interaction prev, VerifyDto source, Verify original) {
    VerifyCalculatedProposal formulas =
        VerifyCalculatedProposal.builder().dto(source).original(original).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public VerifyDto copyWithFormulas(Interaction prev, VerifyDto source) {
    VerifyCalculatedProposal formulas =
        VerifyCalculatedProposal.builder().dto(source).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }
}
