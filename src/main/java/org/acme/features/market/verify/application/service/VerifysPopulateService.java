package org.acme.features.market.verify.application.service;

import java.util.Map;
import java.util.Optional;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.validation.AbstractFailList;
import org.acme.common.validation.ConstraintFail;
import org.acme.features.market.verify.application.VerifyDto;
import org.acme.features.market.verify.application.VerifyPopulateResult;
import org.acme.features.market.verify.domain.gateway.VerifyCursor;
import org.acme.features.market.verify.domain.gateway.VerifyFilter;
import org.acme.features.market.verify.domain.model.valueobject.VerifyNameVO;

import jakarta.enterprise.context.RequestScoped;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class VerifysPopulateService {

  /**
   * @autogenerated PopulateServiceGenerator
   */
  private final VerifysVisibilityService verifyVisibles;

  /**
   * @autogenerated PopulateServiceGenerator
   * @param interaction
   * @param map
   * @param fails
   * @return
   */
  public CompletionStage<Optional<VerifyPopulateResult>> populateInto(Interaction interaction,
      Map<String, String> map, AbstractFailList fails) {
    AbstractFailList constraints = new AbstractFailList();
    VerifyFilter.VerifyFilterBuilder filterBuilder = VerifyFilter.builder();
    Optional<String> hasName = Optional.ofNullable(map.get("name"));
    if (hasName.isPresent()) {
      filterBuilder = filterBuilder.name(hasName.get());
    } else {
      constraints.add(new ConstraintFail("name-required", "The name is required", null));
    }
    return verifyVisibles.listCachedVisibles(interaction, filterBuilder.build(),
        VerifyCursor.builder().limit(1).build()).thenApply(results -> {
          VerifyDto.VerifyDtoBuilder builder = VerifyDto.builder();
          Optional.ofNullable(map.get("Name"))
              .map(value -> VerifyNameVO.tryFrom(value, constraints)).ifPresent(builder::name);
          if (constraints.isEmpty()) {
            return Optional.of(VerifyPopulateResult.builder().original(results.first().orElse(null))
                .dto(builder.build()).build());
          } else {
            fails.add(constraints);
            return Optional.empty();
          }
        });
  }
}
