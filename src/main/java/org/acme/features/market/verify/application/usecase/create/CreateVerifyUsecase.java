package org.acme.features.market.verify.application.usecase.create;

import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.verify.application.service.VerifysVisibilityService;
import org.acme.features.market.verify.application.usecase.create.event.VerifyCreateAllowPipelineStageEvent;
import org.acme.features.market.verify.domain.Verifys;
import org.acme.features.market.verify.domain.gateway.VerifyCacheGateway;
import org.acme.features.market.verify.domain.gateway.VerifyWriteRepositoryGateway;
import org.acme.features.market.verify.domain.model.Verify;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class CreateVerifyUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Verifys aggregate;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final VerifyCacheGateway cache;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<VerifyCreateAllowPipelineStageEvent> createAllow;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final VerifyWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final VerifysVisibilityService visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("verify-create-allow").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      VerifyCreateAllowPipelineStageEvent base =
          VerifyCreateAllowPipelineStageEvent.build(query, true, "Allowed by default");
      createAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<VerifyCreateResult> create(final VerifyCreateCommand query) {
    Span startSpan = tracer.spanBuilder("verify-create").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<Optional<Verify>> create = allow(query).thenCompose(detail -> {
        if (!detail.isAllowed()) {
          throw new NotAllowedException(detail.getDescription());
        }
        return visibility.copyWithFixed(query, query.getDto())
            .thenCompose(builder -> aggregate.initialize(builder.toEntityBuilder(Optional.empty()))
                .thenCompose(verifyEntity -> createAndVerify(query, verifyEntity)));
      });
      return create.thenCompose(verify -> mapEntity(query, verify)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param verifyEntity
   * @return
   */
  private CompletionStage<Optional<Verify>> createAndVerify(final VerifyCreateCommand query,
      final Verify verifyEntity) {
    Span startSpan = tracer.spanBuilder("verify-run-create-and-verify").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      return gateway
          .create(verifyEntity, created -> visibility.checkVisibility(query, created.getUidValue()))
          .thenCompose(validated -> validated
              .map(created -> cache.update(created).thenApply(_ready -> Optional.of(created)))
              .orElseGet(() -> CompletableFuture.completedStage(Optional.empty())))
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @param opverify
   * @return The slide with some values
   */
  private CompletionStage<VerifyCreateResult> mapEntity(final VerifyCreateCommand command,
      final Optional<Verify> opverify) {
    Span startSpan = tracer.spanBuilder("verify-map-created-entity").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      return opverify
          .map(verify -> visibility.copyWithHidden(command, verify)
              .thenApply(visible -> VerifyCreateResult.builder().command(command)
                  .verify(Optional.of(visible)).build()))
          .orElseGet(() -> CompletableFuture.completedStage(
              VerifyCreateResult.builder().command(command).verify(Optional.empty()).build()))
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
