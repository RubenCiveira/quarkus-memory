package org.acme.features.market.verify.application.usecase.list;

import java.util.List;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.verify.application.VerifyDto;
import org.acme.features.market.verify.application.service.VerifysVisibilityService;
import org.acme.features.market.verify.domain.gateway.VerifyCached;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class ListVerifyUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<VerifyListAllow> listAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final VerifysVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public VerifyListAllow allow(final Interaction query) {
    VerifyListAllow base = VerifyListAllow.build(query, true, "Allowed by default");
    listAllow.fire(base);
    return base;
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<VerifyListResult> list(final VerifyListQuery query) {
    CompletionStage<VerifyCached> future = allow(query).getDetail().thenCompose(detail -> {
      if (!detail.isAllowed()) {
        throw new NotAllowedException(detail.getDescription());
      }
      return visibility.listCachedVisibles(query, query.getFilter(), query.getCursor());
    });
    return future.thenCompose(values -> mapList(query, values));
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param verifys
   * @return The slide with some values
   */
  private CompletionStage<VerifyListResult> mapList(final VerifyListQuery query,
      final VerifyCached verifys) {
    List<CompletableFuture<VerifyDto>> futures =
        verifys.getValue().stream().map(verify -> visibility.copyWithHidden(query, verify))
            .map(CompletionStage::toCompletableFuture).toList();
    return CompletableFuture.allOf(futures.toArray(new CompletableFuture[0]))
        .thenApply(voidResult -> VerifyListResult.builder().query(query)
            .verifys(futures.stream().map(CompletableFuture::join).toList())
            .since(verifys.getSince()).build());
  }
}
