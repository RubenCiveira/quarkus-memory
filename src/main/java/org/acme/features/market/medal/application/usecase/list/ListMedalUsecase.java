package org.acme.features.market.medal.application.usecase.list;

import java.util.List;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.medal.application.MedalDto;
import org.acme.features.market.medal.application.service.MedalsVisibilityService;
import org.acme.features.market.medal.application.usecase.list.event.MedalListAllowPipelineStageEvent;
import org.acme.features.market.medal.domain.gateway.MedalCached;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class ListMedalUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<MedalListAllowPipelineStageEvent> listAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final MedalsVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("medal-list-allow").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      MedalListAllowPipelineStageEvent base =
          MedalListAllowPipelineStageEvent.build(query, true, "Allowed by default");
      listAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<MedalListResult> list(final MedalListQuery query) {
    Span startSpan = tracer.spanBuilder("medal-list").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<MedalCached> future = allow(query).thenCompose(detail -> {
        if (!detail.isAllowed()) {
          throw new NotAllowedException(detail.getDescription());
        }
        return visibility.listCachedVisibles(query, query.getFilter(), query.getCursor());
      });
      return future.thenCompose(values -> mapList(query, values)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param medals
   * @return The slide with some values
   */
  private CompletionStage<MedalListResult> mapList(final MedalListQuery query,
      final MedalCached medals) {
    Span startSpan = tracer.spanBuilder("medal-map-list-response").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      List<CompletableFuture<MedalDto>> futures =
          medals.getValue().stream().map(medal -> visibility.copyWithHidden(query, medal))
              .map(CompletionStage::toCompletableFuture).toList();
      return CompletableFuture.allOf(futures.toArray(new CompletableFuture[0]))
          .thenApply(voidResult -> MedalListResult.builder().query(query)
              .medals(futures.stream().map(CompletableFuture::join).toList())
              .since(medals.getSince()).build())
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
