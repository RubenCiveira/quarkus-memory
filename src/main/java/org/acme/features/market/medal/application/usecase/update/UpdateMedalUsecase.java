package org.acme.features.market.medal.application.usecase.update;

import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.medal.application.MedalDto;
import org.acme.features.market.medal.application.service.MedalsVisibilityService;
import org.acme.features.market.medal.domain.Medals;
import org.acme.features.market.medal.domain.gateway.MedalCacheGateway;
import org.acme.features.market.medal.domain.gateway.MedalWriteRepositoryGateway;
import org.acme.features.market.medal.domain.model.Medal;
import org.acme.features.market.medal.domain.model.MedalRef;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class UpdateMedalUsecase {

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Medals aggregate;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final MedalCacheGateway cache;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final MedalWriteRepositoryGateway gateway;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated UpdateUsecaseGenerator
   */
  private final Event<MedalUpdateAllow> updateAllow;

  /**
   * @autogenerated UpdateUsecaseGenerator
   */
  private final MedalsVisibilityService visibility;

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public MedalUpdateAllow allow(final Interaction query, final MedalRef reference) {
    MedalUpdateAllow base =
        MedalUpdateAllow.build(query, Optional.of(reference), true, "Allowed by default");
    updateAllow.fire(base);
    return base;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @param query
   * @return
   */
  public MedalUpdateAllow allow(final Interaction query) {
    MedalUpdateAllow base =
        MedalUpdateAllow.build(query, Optional.empty(), true, "Allowed by default");
    updateAllow.fire(base);
    return base;
  }

  /**
   * @autogenerated UpdateUsecaseGenerator
   * @return
   */
  public MedalUpdateAllow allow() {
    return null;
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<MedalUpdateResult> update(final MedalUpdateCommand command) {
    CompletionStage<Optional<Medal>> updated =
        allow(command, command.getReference()).getDetail().thenCompose(detail -> {
          if (!detail.isAllowed()) {
            throw new NotAllowedException(detail.getDescription());
          }
          return visibility.copyWithFixed(command, command.getDto())
              .thenCompose(builder -> visibility
                  .retrieveVisible(command, command.getReference().getUidValue())
                  .thenCompose(op -> saveIfIsPresent(op, builder)));
        });
    return updated.thenCompose(entity -> mapEntity(command, entity));
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @param opmedal
   * @return The slide with some values
   */
  private CompletionStage<MedalUpdateResult> mapEntity(final MedalUpdateCommand command,
      final Optional<Medal> opmedal) {
    return opmedal
        .map(medal -> visibility.copyWithHidden(command, medal)
            .thenApply(visible -> MedalUpdateResult.builder().command(command)
                .medal(Optional.of(visible)).build()))
        .orElseGet(() -> CompletableFuture.completedStage(
            MedalUpdateResult.builder().command(command).medal(Optional.empty()).build()));
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param original
   * @param dto a filter to retrieve only matching values
   * @return The slide with some values
   */
  private CompletionStage<Optional<Medal>> saveEntity(final Medal original, final MedalDto dto) {
    return aggregate.modify(original, dto.toEntityBuilder(Optional.of(original)))
        .thenCompose(medal -> gateway.update(original, medal))
        .thenCompose(updated -> cache.update(updated).thenApply(_ready -> Optional.of(updated)));
  }

  /**
   * The slide with some values
   *
   * @autogenerated UpdateUsecaseGenerator
   * @param result
   * @param dto a filter to retrieve only matching values
   * @return The slide with some values
   */
  private CompletionStage<Optional<Medal>> saveIfIsPresent(final Optional<Medal> result,
      final MedalDto dto) {
    return result.map(original -> saveEntity(original, dto))
        .orElseGet(() -> CompletableFuture.completedFuture(Optional.empty()));
  }
}
