package org.acme.features.market.medal.domain;

import java.util.Map;
import java.util.Map.Entry;
import java.util.Optional;
import java.util.Set;
import java.util.function.BiFunction;

import org.acme.common.action.ParametrizedFuturePipe;
import org.acme.common.action.PriorityComparator;
import org.acme.features.market.medal.domain.model.Medal;
import org.acme.features.market.medal.domain.model.Medal.MedalBuilder;
import org.acme.features.market.medal.domain.rule.MedalActionType;
import org.acme.features.market.medal.domain.rule.MedalBuilderRule;
import org.acme.features.market.medal.domain.rule.MedalRule;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.inject.Instance;

@RequestScoped
public class Medals {

  /**
   * @autogenerated AggregateGenerator
   */
  private final ParametrizedFuturePipe<MedalBuilder, MedalActionType, Optional<Medal>> builderRules;

  /**
   * @autogenerated AggregateGenerator
   */
  private final ParametrizedFuturePipe<Medal, MedalActionType, Optional<Medal>> rules;

  /**
   * @autogenerated AggregateGenerator
   */
  private Map<String, BiFunction<MedalBuilder, Optional<Medal>, MedalBuilder>> calculatedFields =
      Map.of();

  /**
   * @autogenerated AggregateGenerator
   * @param rules
   * @param builderRules
   */
  public Medals(final Instance<MedalRule> rules, final Instance<MedalBuilderRule> builderRules) {
    this.rules = new ParametrizedFuturePipe<>(MedalActionType.values(), rules.stream().toList(),
        new PriorityComparator<>());
    this.builderRules = new ParametrizedFuturePipe<>(MedalActionType.values(),
        builderRules.stream().toList(), new PriorityComparator<>());
  }

  /**
   * @autogenerated AggregateGenerator
   * @return
   */
  public Set<String> calcultadFields() {
    return calculatedFields.keySet();
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param entity a filter to retrieve only matching values
   * @return The entity
   */
  public Medal clean(final Medal entity) {
    return rules.applyCurrent(MedalActionType.DELETE, entity, Optional.of(entity));
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param builder a filter to retrieve only matching values
   * @return The entity
   */
  public Medal initialize(final MedalBuilder builder) {
    return applyModify(MedalActionType.CREATE, Optional.empty(), builder, null);
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param base a filter to retrieve only matching values
   * @param builder a filter to retrieve only matching values
   * @return The entity
   */
  public Medal modify(final Medal base, final MedalBuilder builder) {
    return applyModify(MedalActionType.UPDATE, Optional.of(base), builder, null);
  }

  /**
   * The entity
   *
   * @autogenerated AggregateGenerator
   * @param type
   * @param original a filter to retrieve only matching values
   * @param builder a filter to retrieve only matching values
   * @param ignore
   * @return The entity
   */
  private Medal applyModify(final MedalActionType type, final Optional<Medal> original,
      final MedalBuilder builder, final String ignore) {
    MedalBuilder ruledBuilder = this.builderRules.applyCurrent(type, builder, original);
    for (Entry<String, BiFunction<MedalBuilder, Optional<Medal>, MedalBuilder>> entry : calculatedFields
        .entrySet()) {
      if (!entry.getKey().equals(ignore)) {
        ruledBuilder = entry.getValue().apply(ruledBuilder, original);
      }
    }
    return rules.applyCurrent(type, ruledBuilder.buildValid(), original);
  }
}
