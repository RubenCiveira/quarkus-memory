package org.acme.features.market.medal.infrastructure.driven;

import java.time.OffsetDateTime;
import java.util.List;
import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.features.market.medal.domain.gateway.MedalCacheGateway;
import org.acme.features.market.medal.domain.gateway.MedalCached;
import org.acme.features.market.medal.domain.gateway.MedalCursor;
import org.acme.features.market.medal.domain.gateway.MedalFilter;
import org.acme.features.market.medal.domain.model.Medal;

import io.quarkus.cache.Cache;
import io.quarkus.cache.CacheName;
import jakarta.enterprise.context.RequestScoped;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class MedalCacheGatewayAdapter implements MedalCacheGateway {

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   */
  @CacheName("medal")
  final Cache cache;

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param medal
   * @return
   */
  public CompletionStage<Void> remove(final Medal medal) {
    return invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  public CompletionStage<Optional<MedalCached>> retrieve(final MedalFilter filter,
      final MedalCursor cursor) {
    String key = key(filter, cursor);
    return cache.<String, MedalCached>get(key, k -> null).subscribeAsCompletionStage()
        .thenCompose(cached -> cached == null ? notInCache(key)
            : CompletableFuture.completedStage(Optional.of(cached)));
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @param medals
   * @return
   */
  public CompletionStage<Void> store(final MedalFilter filter, final MedalCursor cursor,
      final List<Medal> medals) {
    String key = key(filter, cursor);
    return cache
        .<String, MedalCached>get(key,
            k -> MedalCached.builder().since(OffsetDateTime.now()).value(medals).build())
        .subscribeAsCompletionStage().<Void>thenApply(chitem -> null);
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param medal
   * @return
   */
  public CompletionStage<Void> update(final Medal medal) {
    return invalidate();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @return
   */
  private CompletableFuture<Void> invalidate() {
    return cache.invalidateAll().subscribeAsCompletionStage();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param filter
   * @param cursor
   * @return
   */
  private String key(final MedalFilter filter, final MedalCursor cursor) {
    return filter.toString() + "-" + cursor.toString();
  }

  /**
   * @autogenerated CacheAdaterGatewayGenerator
   * @param key
   * @return
   */
  private CompletableFuture<Optional<MedalCached>> notInCache(final String key) {
    return cache.invalidateIf(cachekey -> cachekey.equals(key)).subscribeAsCompletionStage()
        .thenApply(_inv -> Optional.empty());
  }
}
