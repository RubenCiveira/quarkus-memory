package org.acme.features.market.medal.application.usecase.create;

import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.medal.application.service.MedalsVisibilityService;
import org.acme.features.market.medal.domain.Medals;
import org.acme.features.market.medal.domain.gateway.MedalCacheGateway;
import org.acme.features.market.medal.domain.gateway.MedalWriteRepositoryGateway;
import org.acme.features.market.medal.domain.model.Medal;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class CreateMedalUsecase {

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final Medals aggregate;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final MedalCacheGateway cache;

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated CreateUsecaseGenerator
   */
  private final Event<MedalCreateAllow> createAllow;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final MedalWriteRepositoryGateway gateway;

  /**
   * @autogenerated CreateUsecaseGenerator
   */
  private final MedalsVisibilityService visibility;

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @return
   */
  public MedalCreateAllow allow(final Interaction query) {
    MedalCreateAllow base = MedalCreateAllow.build(query, true, "Allowed by default");
    createAllow.fire(base);
    return base;
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<MedalCreateResult> create(final MedalCreateCommand query) {
    CompletionStage<Optional<Medal>> create = allow(query).getDetail().thenCompose(detail -> {
      if (!detail.isAllowed()) {
        throw new NotAllowedException(detail.getDescription());
      }
      return visibility.copyWithFixed(query, query.getDto())
          .thenCompose(builder -> aggregate.initialize(builder.toEntityBuilder(Optional.empty()))
              .thenCompose(medalEntity -> createAndVerify(query, medalEntity)));
    });
    return create.thenCompose(medal -> mapEntity(query, medal));
  }

  /**
   * @autogenerated CreateUsecaseGenerator
   * @param query
   * @param medalEntity
   * @return
   */
  private CompletionStage<Optional<Medal>> createAndVerify(final MedalCreateCommand query,
      final Medal medalEntity) {
    return gateway
        .create(medalEntity, created -> visibility.checkVisibility(query, created.getUidValue()))
        .thenCompose(validated -> validated
            .map(created -> cache.update(created).thenApply(_ready -> Optional.of(created)))
            .orElseGet(() -> CompletableFuture.completedStage(Optional.empty())));
  }

  /**
   * The slide with some values
   *
   * @autogenerated CreateUsecaseGenerator
   * @param command a filter to retrieve only matching values
   * @param opmedal
   * @return The slide with some values
   */
  private CompletionStage<MedalCreateResult> mapEntity(final MedalCreateCommand command,
      final Optional<Medal> opmedal) {
    return opmedal
        .map(medal -> visibility.copyWithHidden(command, medal)
            .thenApply(visible -> MedalCreateResult.builder().command(command)
                .medal(Optional.of(visible)).build()))
        .orElseGet(() -> CompletableFuture.completedStage(
            MedalCreateResult.builder().command(command).medal(Optional.empty()).build()));
  }
}
