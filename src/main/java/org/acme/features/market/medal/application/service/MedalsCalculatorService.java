package org.acme.features.market.medal.application.service;

import org.acme.common.action.Interaction;
import org.acme.features.market.medal.application.MedalDto;
import org.acme.features.market.medal.application.service.proposal.MedalCalculatedProposal;
import org.acme.features.market.medal.domain.model.Medal;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class MedalsCalculatorService {

  /**
   * Event source for maniputale the visible list
   *
   * @autogenerated CalculatorServiceGenerator
   */
  private final Event<MedalCalculatedProposal> formulaBuilder;

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @param original The source interaction
   * @return The input entity with the copy values without hidden
   */
  public MedalDto copyWithFormulas(Interaction prev, MedalDto source, Medal original) {
    MedalCalculatedProposal formulas =
        MedalCalculatedProposal.builder().dto(source).original(original).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }

  /**
   * The input entity with the copy values without hidden
   *
   * @autogenerated CalculatorServiceGenerator
   * @param prev The source interaction
   * @param source The source interaction
   * @return The input entity with the copy values without hidden
   */
  public MedalDto copyWithFormulas(Interaction prev, MedalDto source) {
    MedalCalculatedProposal formulas =
        MedalCalculatedProposal.builder().dto(source).interaction(prev).build();
    formulaBuilder.fire(formulas);
    return formulas.getDto();
  }
}
