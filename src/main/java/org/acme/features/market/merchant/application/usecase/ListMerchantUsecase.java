package org.acme.features.market.merchant.application.usecase;

import java.util.List;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.exception.NotAllowedException;
import org.acme.features.market.merchant.application.allow.MerchantListAllow;
import org.acme.features.market.merchant.application.interaction.MerchantDto;
import org.acme.features.market.merchant.application.interaction.query.MerchantAllowQuery;
import org.acme.features.market.merchant.application.interaction.query.MerchantListQuery;
import org.acme.features.market.merchant.application.interaction.result.MerchantListResult;
import org.acme.features.market.merchant.application.usecase.service.MerchantsVisibilityService;
import org.acme.features.market.merchant.domain.model.Merchant;

import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class ListMerchantUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<MerchantListAllow> listAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final MerchantsVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public MerchantListAllow allow(final MerchantAllowQuery query) {
    MerchantListAllow base = MerchantListAllow.build(true, "Allowed by default");
    listAllow.fire(base);
    return base;
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public MerchantListResult list(final MerchantListQuery query) {
    CompletionStage<List<Merchant>> future = allow(query).getDetail().thenCompose(detail -> {
      if (!detail.isAllowed()) {
        throw new NotAllowedException(detail.getDescription());
      }
      return visibility.listVisibles(query, query.getFilter(), query.getCursor());
    });
    return MerchantListResult.builder().query(query)
        .merchants(future.thenCompose(values -> mapList(query, values))).build();
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  private MerchantListAllow allow(final MerchantListQuery query) {
    return allow(MerchantAllowQuery.builder().build(query));
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param merchants
   * @return The slide with some values
   */
  private CompletionStage<List<MerchantDto>> mapList(final MerchantListQuery query,
      final List<Merchant> merchants) {
    List<CompletableFuture<MerchantDto>> futures =
        merchants.stream().map(merchant -> visibility.copyWithHidden(query, merchant))
            .map(CompletionStage::toCompletableFuture).toList();
    return CompletableFuture.allOf(futures.toArray(new CompletableFuture[0]))
        .thenApply(voidResult -> futures.stream().map(CompletableFuture::join).toList());
  }
}
