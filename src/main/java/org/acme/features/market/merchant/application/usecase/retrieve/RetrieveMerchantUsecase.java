package org.acme.features.market.merchant.application.usecase.retrieve;

import java.time.OffsetDateTime;
import java.util.Optional;
import java.util.concurrent.CompletableFuture;
import java.util.concurrent.CompletionStage;

import org.acme.common.action.Interaction;
import org.acme.common.exception.NotAllowedException;
import org.acme.common.security.Allow;
import org.acme.features.market.merchant.application.service.MerchantsVisibilityService;
import org.acme.features.market.merchant.application.usecase.retrieve.event.MerchantRetrieveAllowPipelineStageEvent;
import org.acme.features.market.merchant.domain.gateway.MerchantCached;
import org.acme.features.market.merchant.domain.model.MerchantRef;

import io.opentelemetry.api.trace.Span;
import io.opentelemetry.api.trace.StatusCode;
import io.opentelemetry.api.trace.Tracer;
import io.opentelemetry.context.Scope;
import jakarta.enterprise.context.RequestScoped;
import jakarta.enterprise.event.Event;
import lombok.RequiredArgsConstructor;

@RequestScoped
@RequiredArgsConstructor
public class RetrieveMerchantUsecase {

  /**
   * Event source for maniputale the visibility filter over the entity fields
   *
   * @autogenerated ListUsecaseGenerator
   */
  private final Event<MerchantRetrieveAllowPipelineStageEvent> retrieveAllow;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final Tracer tracer;

  /**
   * @autogenerated ListUsecaseGenerator
   */
  private final MerchantsVisibilityService visibility;

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @param reference
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query, final MerchantRef reference) {
    Span startSpan = tracer.spanBuilder("merchant-specific-allow-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      MerchantRetrieveAllowPipelineStageEvent base = MerchantRetrieveAllowPipelineStageEvent
          .build(query, Optional.of(reference), true, "Allowed by default");
      retrieveAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * @autogenerated ListUsecaseGenerator
   * @param query
   * @return
   */
  public CompletionStage<Allow> allow(final Interaction query) {
    Span startSpan = tracer.spanBuilder("merchant-generic-allow-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      MerchantRetrieveAllowPipelineStageEvent base = MerchantRetrieveAllowPipelineStageEvent
          .build(query, Optional.empty(), true, "Allowed by default");
      retrieveAllow.fire(base);
      return base.getDetail().whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setAttribute("allowed", val.isAllowed());
          startSpan.setAttribute("reason", val.getDescription());
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @return The slide with some values
   */
  public CompletionStage<MerchantRetrieveResult> retrieve(final MerchantRetrieveQuery query) {
    Span startSpan = tracer.spanBuilder("merchant-retrieve").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      CompletionStage<MerchantCached> result =
          allow(query, query.getReference()).thenCompose(detail -> {
            if (!detail.isAllowed()) {
              throw new NotAllowedException(detail.getDescription());
            }
            return visibility.retrieveCachedVisible(query, query.getReference().getUidValue());
          });
      return result.thenCompose(op -> this.mapEntity(query, op)).whenComplete((val, ex) -> {
        if (null == ex) {
          startSpan.setStatus(StatusCode.OK);
        } else {
          startSpan.recordException(ex).setStatus(StatusCode.ERROR);
        }
        startSpan.end();
      });
    }
  }

  /**
   * The slide with some values
   *
   * @autogenerated ListUsecaseGenerator
   * @param query a filter to retrieve only matching values
   * @param opmerchant
   * @return The slide with some values
   */
  private CompletionStage<MerchantRetrieveResult> mapEntity(final MerchantRetrieveQuery query,
      final MerchantCached opmerchant) {
    Span startSpan = tracer.spanBuilder("merchant-map-retrieve-result").startSpan();
    try (Scope scope = startSpan.makeCurrent()) {
      return opmerchant.first()
          .map(merchant -> visibility.copyWithHidden(query, merchant)
              .thenApply(merchantWithHidden -> MerchantRetrieveResult.builder().interaction(query)
                  .merchant(Optional.of(merchantWithHidden)).since(opmerchant.getSince()).build()))
          .orElseGet(() -> CompletableFuture.completedFuture(MerchantRetrieveResult.builder()
              .interaction(query).merchant(Optional.empty()).since(OffsetDateTime.now()).build()))
          .whenComplete((val, ex) -> {
            if (null == ex) {
              startSpan.setStatus(StatusCode.OK);
            } else {
              startSpan.recordException(ex).setStatus(StatusCode.ERROR);
            }
            startSpan.end();
          });
    }
  }
}
