package org.acme.features.market.merchant.application.service.event;

import java.util.Optional;
import java.util.concurrent.CompletionStage;
import java.util.function.Consumer;

import org.acme.common.action.Interaction;
import org.acme.features.market.merchant.application.MerchantDto;
import org.acme.features.market.merchant.domain.model.Merchant;

import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NonNull;
import lombok.experimental.SuperBuilder;

@Data
@SuperBuilder(toBuilder = true)
@EqualsAndHashCode(callSuper = true, onlyExplicitlyIncluded = true)
public class MerchantFormulaBuilder extends Interaction {

  /**
   * @autogenerated VisibleContentGenerator
   */
  Merchant original;

  /**
   * @autogenerated VisibleContentGenerator
   */
  @NonNull
  private CompletionStage<MerchantDto> dto;

  /**
   * @autogenerated VisibleContentGenerator
   * @return
   */
  public Optional<Merchant> getOriginal() {
    return Optional.ofNullable(original);
  }

  /**
   * @autogenerated VisibleContentGenerator
   * @param mapper
   */
  public void tap(Consumer<MerchantDto> mapper) {
    dto = dto.thenApply(dto -> {
      mapper.accept(dto);
      return dto;
    });
  }
}
